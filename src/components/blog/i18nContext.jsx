export const LanguageProvider = ({ children }) => {
  const [lang, setLang] = useState(() => localStorage.getItem('blog_lang') || 'en');

  useEffect(() => {
    if (!localStorage.getItem('blog_lang')) {
      fetch('https://ipapi.co/json/')
        .then(r => r.json())
        .then(data => {
          const detected = IP_TO_LANG[data.country_code] || 'en';
          setLang(detected);
          localStorage.setItem('blog_lang', detected);
        })
        .catch(() => {});
    }
  }, []);

  const changeLang = (code) => {
    setLang(code);
    localStorage.setItem('blog_lang', code);
    window.dispatchEvent(new CustomEvent('blog_lang_change', { detail: code }));
  };

  const t = (key) => {
    return translations[lang]?.[key] || translations.en[key] || key;
  };

  return (
    <LanguageContext.Provider value={{ lang, changeLang, t, BLOG_LANGS }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLanguage must be used within LanguageProvider');
  return ctx;
};